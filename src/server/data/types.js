import { readFileSync } from "fs";

/**
 * Combines the split tag value from JSDoc tag extraction.
 * The extractor splits "Scheme Events" into { name: "Scheme", description: "Events" }.
 */
function tagValue(tag) {
  if (!tag) return undefined;
  return [tag.name, tag.description].filter(Boolean).join(" ");
}

/**
 * Extracts the first sentence from a JSDoc description for use as metaDescription.
 * Strips markdown links/backticks and stops at the first period followed by whitespace.
 */
function firstSentence(description) {
  if (!description) return "";
  // Strip markdown links: [text](url) → text, and backticks
  const plain = description
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/`/g, "");
  // Collapse whitespace then match first sentence ending with a period
  const collapsed = plain.replace(/\s+/g, " ").trim();
  const match = collapsed.match(/^(.+?\.)\s/);
  return match ? match[1] : collapsed.split(".")[0] + ".";
}

/**
 * Builds breadcrumb trail from the URL path.
 * "/api/bus-events" → [API, Bus events]
 * "/api/events/esp-events" → [API, Events, ESP Events]
 */
function buildCrumbs(url, pageTitle, allTypes) {
  // Determine the root segment from the URL
  const segments = url.replace(/^\//, "").split("/");
  const root = segments[0] || "api";
  const rootLabel = root.charAt(0).toUpperCase() + root.slice(1);
  const crumbs = [{ label: rootLabel, url: `/${root}` }];

  if (segments.length > 2) {
    const parentUrl = `/${segments[0]}/${segments[1]}`;
    let parent = allTypes.find((t) => t.url === parentUrl);

    if (!parent) {
      parent = allTypes.find(
        (t) =>
          t.url !== url &&
          t.url.startsWith(parentUrl + "/") &&
          t.menuLabel,
      );
    }

    if (parent) {
      crumbs.push({
        label: parent.menuLabel || parent.pageTitle,
        url: parent.url,
      });
    }
  }

  crumbs.push({ label: pageTitle, url });
  return crumbs;
}

/**
 * Creates an Eleventy data function that reads a type manifest
 * (generated by the WTFM type extractor) and returns a
 * pagination-ready array of type documentation objects.
 *
 * The returned array follows the same shape as
 * `createComponentsData`, so Eleventy templates can treat both
 * component pages and type pages uniformly.
 *
 * @param {object} options
 * @param {string} options.typeManifestPath - Absolute path to type-manifest.json
 * @returns {function} Eleventy data function
 */
export default function createTypeDocsData(options = {}) {
  const { typeManifestPath } = options;

  return () => {
    let manifest;
    try {
      manifest = JSON.parse(readFileSync(typeManifestPath, "utf-8"));
    } catch {
      console.warn(
        "wtfm/data/types.js: Could not read type-manifest.json at",
        typeManifestPath,
      );
      return [];
    }

    // First pass: collect all declarations with @docUrl
    const raw = [];
    for (const mod of manifest.modules) {
      for (const decl of mod.declarations) {
        const url = tagValue(decl.docUrl);
        if (!url) continue;

        raw.push({
          name: decl.name,
          kind: decl.kind,
          url,
          menuLabel: tagValue(decl.menuLabel),
          menuIcon: tagValue(decl.menuIcon),
          pageTitle:
            tagValue(decl.docPageTitle) ||
            tagValue(decl.menuLabel) ||
            decl.name,
          metaDescription: firstSentence(decl.description),
        });
      }
    }

    // Sort alphabetically by URL for stable ordering
    raw.sort((a, b) => a.url.localeCompare(b.url));

    // Second pass: build breadcrumbs (needs the full list for parent lookups)
    for (const item of raw) {
      item.crumbs = buildCrumbs(item.url, item.pageTitle, raw);
    }

    return raw;
  };
}
